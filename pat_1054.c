/*
1054. 求平均值 (20)
本题的基本要求非常简单：给定N个实数，计算它们的平均值。但复杂的是有些输入数据可能是非法的。一个“合法”的输入是[-1000，1000]区间内的实数，并且最多精确到小数点后2位。当你计算平均值的时候，不能把那些非法的数据算在内。
输入格式： 
输入第一行给出正整数N（<=100）。随后一行给出N个实数，数字间以一个空格分隔。 
输出格式： 
对每个非法输入，在一行中输出“ERROR: X is not a legal number”，其中X是输入。最后在一行中输出结果：“The average of K numbers is Y”，其中K是合法输入的个数，Y是它们的平均值，精确到小数点后2位。如果平均值无法计算，则用“Undefined”替换Y。如果K为1，则输出“The average of 1 number is Y”。 
输入样例1：
7
5a -3.200 aaa 9999 .2.3.4 7.123 2.35
输出样例1：
ERROR: aaa is not a legal number
ERROR: 9999 is not a legal number
ERROR: 2.3.4 is not a legal number
ERROR: 7.123 is not a legal number
The average of 3 numbers is 1.38
输入样例2：
2
aaa -9999
输出样例2：
ERROR: aaa is not a legal number
ERROR: -9999 is not a legal number
The average of 0 numbers is Undefined
*/
int jude(char s1[],float *sum){
	int i=0;
	float a=0,b=0,n=1;
	int flag=0;
	int num_point=0;
	*sum=0;
	if(s1[0]=='.'){
		return 0;
	}
	if(s1[0]=='-'){
		i++;
	}
	while(s1[i]){
		if(s1[i]>='0'&&s1[i]<='9'){
			if(num_point==0){
				a=a*10+s1[i]-'0';
			}
			else if(num_point==1){
				flag++;
				if(flag>2){
					return 0;
				}
				n/=10;
				b+=((s1[i]-'0')*n);
			}
		}
		else if(s1[i]=='.'){
			num_point++;
			if(num_point>1){
				return 0;
			}
		}
		else{
			return 0;
		}
		i++;
	}
	*sum=a+b;
	if(s1[0]=='-'){
		*sum=(*sum)*-1;
	}
	return 1;
}
#include<stdio.h>
#include<math.h>
int main(){
	char s1[100000];
	int N=0,i=0,num=0,flag=0;
	float sum=0;
	float all=0;
	scanf("%d",&N);
	while(N--){
		scanf("%s",&s1);
 		if(jude(s1,&sum)==1){
			if(sum>=-1000&&sum<=1000){
				num++;
				all+=sum;
			}
			else{
				printf("ERROR: %s is not a legal number\n",s1);
			}
		}
		else{
			printf("ERROR: %s is not a legal number\n",s1);
		}
	}
	if(num==0){
		printf("The average of 0 numbers is Undefined\n");
	}
	else if(num==1){
		printf("The average of 1 number is %0.2f\n",all);
	}
	else {
		printf("The average of %d numbers is %0.2f\n",num,all/num);
	}
	return 0;
}
