/*
1024. 科学计数法 (20)

科学计数法是科学家用来表示很大或很小的数字的一种方便的方法，
其满足正则表达式[+-][1-9]"."[0-9]+E[+-][0-9]+，
即数字的整数部分只有1位，小数部分至少有1位，
该数字及其指数部分的正负号即使对正数也必定明确给出。
现以科学计数法的格式给出实数A，请编写程序按普通数字表示法输出A，
并保证所有有效位都被保留。
输入格式： 
每个输入包含1个测试用例，即一个以科学计数法表示的实数A。
该数字的存储长度不超过9999字节，且其指数的绝对值不超过9999。
输出格式： 
对每个测试用例，在一行中按普通数字表示法输出A，
并保证所有有效位都被保留，包括末尾的0。
输入样例1：
+1.23400E-03
输出样例1：
0.00123400
输入样例2：
-1.2E+10
输出样例2：
-12000000000
*/
#include<stdio.h>
#include<string.h>
int main(){
	char a[10010],ch;
	int LONG=0,flag=0,i=0,j=1;
	int INDEX=0;
	scanf("%s",&a);
	LONG=strlen(a);
	if(a[0]=='-'){
		printf("-");
	}
	for(i=1;i<LONG;i++){
		if(a[i]=='E'){
			flag=i;
			break;
		}
	}
	i++;
	ch=a[i];
	for(++i;i<LONG;i++){
		INDEX=INDEX*10+(a[i]-'0');
	}
	if(ch=='-'){
		printf("0.");
		for(i=1;i<INDEX;i++){
			printf("0");
		}
		for(i=1;i<flag;i++){
			if(a[i]=='.'){
				continue;
			}
			printf("%c",a[i]);
		}
	}
	if(ch=='+'){
		for(i=0;i<=INDEX+1;i++){
			if(j<flag){
				if(a[j]=='.'){
					j++;
					continue;
				}
				printf("%c",a[j]);
			}
			else{
				printf("0");
			}
			j++;
		}
		if(j<flag){
			printf(".");
			while(j<flag){
				printf("%c",a[j]);
				j++;
			}
		}
	}
	return 0;
}
