/*
1027. 打印沙漏(20)

本题要求你写个程序把给定的符号打印成沙漏的形状。
例如给定17个“*”，要求按下列格式打印
*****
 ***
  *
 ***
*****
所谓“沙漏形状”，是指每行输出奇数个符号；各行符号中心对齐；
相邻两行符号数差2；符号数先从大到小顺序递减到1，
再从小到大顺序递增；首尾符号数相等。
给定任意N个符号，不一定能正好组成一个沙漏。
要求打印出的沙漏能用掉尽可能多的符号。
输入格式： 
输入在一行给出1个正整数N（<=1000）和一个符号，中间以空格分隔。 
输出格式： 
首先打印出由给定符号组成的最大的沙漏形状，
最后在一行中输出剩下没用掉的符号数。 
输入样例：
19 *
输出样例：
*****
 ***
  *
 ***
*****
2
*/
#include<stdio.h>
int main(){
	char ch;
	int N=0,sum=1;
	int i=1,j=0,flag_up=0,flag_down=0;
	scanf("%d %c",&N,&ch); 
	for(i=3;i<=N;i=i+2){
		sum+=2*i;
		if(sum>N){
			break;
		}
	} 
	if(N==1||N==2){
		i=3;
		sum=7;
	}

	flag_down=flag_up=i-2;
	sum=sum-2*i;
	N=N-sum;
	while(flag_up>=1){
		for(i=1;i<=j;i++){
			printf(" ");
		}
		for(i=j+1;i<=j+flag_up;i++){
			printf("%c",ch);
		}
		printf("\n");
		flag_up=flag_up-2;
		j++;
	}
	j=j-2;
	flag_up=flag_up+4;
	while(flag_up<=flag_down){
		for(i=1;i<=j;i++){
			printf(" ");
		}
		for(i=j+1;i<=j+flag_up;i++){
			printf("%c",ch);
		}
		printf("\n");
		flag_up=flag_up+2;
		j--;
	}
	printf("%d",N);
	return 0;
}
