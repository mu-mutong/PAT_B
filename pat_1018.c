/*
1018. 锤子剪刀布 (20)
大家应该都会玩“锤子剪刀布”的游戏：
两人同时给出手势，胜负规则如图所示：
现给出两人的交锋记录，请统计双方的胜、平、负次数，
并且给出双方分别出什么手势的胜算最大。 
输入格式： 
输入第1行给出正整数N（<=105），即双方交锋的次数。
随后N行，每行给出一次交锋的信息，即甲、乙双方同时给出的的手势。
C代表“锤子”、J代表“剪刀”、B代表“布”，
第1个字母代表甲方，第2个代表乙方，中间有1个空格。 
输出格式： 
输出第1、2行分别给出甲、乙的胜、平、负次数，数字间以1个空格分隔。
第3行给出两个字母，分别代表甲、乙获胜次数最多的手势，中间有1个空格。
如果解不唯一，则输出按字母序最小的解。 

输入样例：
10
C J
J B
C B
B B
B C
C C
C B
J B
B C
J J
输出样例：
5 3 2
2 3 5
B B*/
#include<stdio.h>
int main(){
	int i=0,n;
	char jia,yi,k;
	int win=0,lose=0,draw=0;
	int win1_C=0,win1_B=0,win1_J=0;
	int win2_C=0,win2_B=0,win2_J=0;
	scanf("%d",&n);
	for(i=0;i<n;i++){
		k=getchar();
	
		scanf("%c %c",&jia,&yi);
		
		
		if(jia=='B'){
			if(yi=='B'){
				draw++;
			}
			else if(yi=='C'){
				win++;
				win1_B++;
			}
			else{
				lose++;
				win2_J++;
			} 
		}
		if(jia=='C'){
			if(yi=='B'){
				lose++;
				win2_B++;
			}
			else if(yi=='C'){
				draw++;
			}
			else{
				win++;
				win1_C++;
			} 
		}
		if(jia=='J'){
			if(yi=='B'){
				win++;
				win1_J++;
			}
			else if(yi=='C'){
				lose++;
				win2_C++; 
			}
			else{
				draw++;
			} 
		}
	} 
	if(win1_C>win1_B){
		if(win1_C>=win1_J){
			jia='C';
		}
		else{
			jia='J';
		}
	}
	else{
		if(win1_B>=win1_J){
			jia='B';
		}
		else{
			jia='J';
		}
	}
	if(win2_C>win2_B){
		if(win2_C>=win2_J){
			yi='C';
		}
		else{
			yi='J';
		}
	}
	else{
		if(win2_B>=win2_J){
			yi='B';
		}
		else{
			yi='J';
		}
	}
	printf("%d %d %d\n",win,draw,lose);
	printf("%d %d %d\n",lose,draw,win);
	printf("%c %c\n",jia,yi);
	return 0;
}
