/*
1074. 宇宙无敌加法器(20)

地球人习惯使用十进制数，并且默认一个数字的每一位都是十进制的。而在PAT星人开挂的世界里，每个数字的每一位都是不同进制的，这种神奇的数字称为“PAT数”。每个PAT星人都必须熟记各位数字的进制表，例如“……0527”就表示最低位是7进制数、第2位是2进制数、第3位是5进制数、第4位是10进制数，等等。每一位的进制d或者是0（表示十进制）、或者是[2，9]区间内的整数。理论上这个进制表应该包含无穷多位数字，但从实际应用出发，PAT星人通常只需要记住前20位就够用了，以后各位默认为10进制。 
在这样的数字系统中，即使是简单的加法运算也变得不简单。例如对应进制表“0527”，该如何计算“6203+415”呢？我们得首先计算最低位：3+5=8；因为最低位是7进制的，所以我们得到1和1个进位。第2位是：0+1+1（进位）=2；因为此位是2进制的，所以我们得到0和1个进位。第3位是：2+4+1（进位）=7；因为此位是5进制的，所以我们得到2和1个进位。第4位是：6+1（进位）=7；因为此位是10进制的，所以我们就得到7。最后我们得到：6203+415=7201。 
输入格式： 
输入首先在第一行给出一个N位的进制表（0 < N <=20），以回车结束。 随后两行，每行给出一个不超过N位的正的PAT数。 
输出格式： 
在一行中输出两个PAT数之和。 
输入样例：
10527
2203
92415
输出样例：
7201
10527
96203
96415

*/
#include<stdio.h>
#include<string.h>
int main(){
	int  sum1[22],sum2[22],ans[22],a[22];
	char s1[22];
	int i=0,j=0,k=0,len1=0,len2=0,len3=0,flag=0;
	
	for(i=0;i<22;i++){
		sum1[i]=0;
		sum2[i]=0;
		ans[i]=0;
		a[i]=10;
	}
	scanf("%s",&s1);
	i=strlen(s1)-1;
	while(i>=0){
		a[j++]=s1[i--]-'0';
		if(a[j-1]==0){
			a[j-1]=10;
		}
	
	}
	len1=j;
	scanf("%s",&s1);
	i=strlen(s1)-1;
	j=0;
	while(i>=0){
		sum1[j++]=s1[i--]-'0';
	}
	len2=j;
	scanf("%s",&s1);
	i=strlen(s1)-1;
	j=0;
	while(i>=0){
		sum2[j++]=s1[i--]-'0';
	}
	len3=j;
	i=0;
	j=0;
	while(i<len2&&j<len3){
		ans[k++]=sum1[i++]+sum2[j++];
	}
	while(i<len2){
		ans[k++]=sum1[i++];
	}
	while(j<len3){
		ans[k++]=sum2[j++];
	}
	for(i=0;i<k;i++){
		ans[i+1]+=ans[i]/a[i];
		ans[i]=ans[i]%a[i];
	}
	if(ans[i]>0){
		ans[i+1]+=ans[i]/a[i];
		ans[i]=ans[i]%a[i];
		k++;
		i++;
	}
	else{
		k--;
	}
	if(ans[i]>0){
		ans[i+1]+=ans[i]/a[i];
		ans[i]=ans[i]%a[i];
		k++;
	}
	for(i=k;i>=0;i--){
		if(!flag&&ans[i]==0){
			continue;
		}
		else{
			printf("%d",ans[i]);
			flag=1;
		}
	}
	if(!flag){
		printf("0");
	} 
	return 0;
}
